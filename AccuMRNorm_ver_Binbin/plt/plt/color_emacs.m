function COL = color_emacs(cname)
%COLOR_EMACS - color(s) in Emacs
%  COL = COLOR_EMACS(COLOR_NAME)
%  COL = COLOR_EMACS('all')
%
%  EXAMPLE :
%    col = color_emacs('AliceBlue');
%    col = color_emacs('all');
%
%  VERSION :
%    0.90 20.06.12 YM  pre-release
%
%  See also colormap

if nargin < 1,  cname = 'all';  end

if strcmpi(cname,'Read&Print'),
  fp = fileparts(mfilename('fullpath'));
  txt = txt_read(fullfile(fp,'colors2.txt'));
  fid = fopen(fullfile(fp,'tmp.m'),'w');
  try
    for N = 1:length(txt)
      tmptxt = txt{N};
      tmpidx1 = strfind(tmptxt,'(');
      tmpidx2 = strfind(tmptxt,')');
      tmpname = tmptxt(1:tmpidx1(1)-1);
      tmprgb  = tmptxt(tmpidx1(1)+1:tmpidx2(1)-1);
      tmpname = deblank(tmpname);
      tmpname(end+1:21) = ' ';
      fprintf(fid,'C.%s = [%3d %3d %3d];\n',tmpname,str2num(tmprgb));
    end
  catch
    fclose(fid);
  end
  fclose(fid);
  COL = [];
  return
end


